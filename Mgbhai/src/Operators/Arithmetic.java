package Operators;

public class Arithmetic {
public static void main(String args) {
	byte a = 20, b = 40;
	System.out.println("value of a+b="+ (a+b));
	System.out.println("value of a*b="+ (a*b));
	System.out.println("value of a-b="+ (a-b));
	System.out.println("value of a/b="+ (a/b));
	System.out.println("value of a%b="+ (a%b));
		
	
	
		
		
		
		
		
		
	}

}
