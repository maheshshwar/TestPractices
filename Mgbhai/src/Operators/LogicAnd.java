package Operators;

public class LogicAnd {
public static void main(String[] args) {
	// && operator 
	System.out.println((12>25) && (20>30));
	System.out.println((10<5) && (20>15));
	// || or operator
	System.out.println((50>25) || (60>40));
	System.out.println((30<10) || (20>15));
	// not operator !
	System.out.println(!(6==7));
	System.out.println(!(6==7));
	
	
	
}
	


}
