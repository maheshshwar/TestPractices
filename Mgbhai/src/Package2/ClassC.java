package Package2;

public class ClassC {
	
	public int a = 60;
	
	private int b = 50;
	
	int c = 40;
	
	protected int d = 30;
	
	
	public static void main(String[] args) {
		
		ClassC x = new ClassC ();
		
		System.out.println(x.a);
		System.out.println(x.b);
		System.out.println(x.c);
		System.out.println(x.d);
		
	}

}
