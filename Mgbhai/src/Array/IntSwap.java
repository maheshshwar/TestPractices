package Array;

public class IntSwap {
	
	public static void main(String[] args) {
		
		int a = 80;
		int b = 90;
		
		System.out.println("Swapping integer"); // x
		
		int x ;
		
		x=a; // 80
		
		a=b; // 90
		
		b=x; // 80
		
		System.out.println(a);
		System.out.println(b);
		
		
		
		
	}
	

}
